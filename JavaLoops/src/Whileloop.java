
public class Whileloop {

	public static void main(String[] args) {
		int i=1;
		
		// while loop executes only when the condition mentioned is true
		while(i<=10){
			System.out.println("i is ="+ i);
			i++;
		} // this braces define the scope of the while loop.
		
		/*
		 * if we put ; after while(i<10); then compiler will not go further
		 * if not braces then ';' defines the scope of the loop
		 * 
		 * entry criteria -  loop condition must be true
		 * exit criteria - loop condition is false
		 * 
		 * loops are not statements- they dont terminate, they exits.
		 */

		
		
		
		
	}

}
